package edu.poli.prap.pp.service;

import edu.poli.prap.pp.data.Step;
import java.util.List;
import javax.ejb.Stateless;
import javax.persistence.EntityManager;
import javax.persistence.PersistenceContext;
import javax.ws.rs.Consumes;
import javax.ws.rs.DELETE;
import javax.ws.rs.GET;
import javax.ws.rs.POST;
import javax.ws.rs.PUT;
import javax.ws.rs.Path;
import javax.ws.rs.PathParam;
import javax.ws.rs.Produces;

/**
 *
 * @author User
 */
@Stateless
@Path("edu.poli.prap.pp.data.step")
public class StepFacadeREST extends AbstractFacade<Step> {

    @PersistenceContext(unitName = "LogicaPU")
    private EntityManager em;

    public StepFacadeREST() {
        super(Step.class);
    }

    @POST
    @Override
    @Consumes({"application/xml", "application/json"})
    public void create(Step entity) {
        super.create(entity);
    }

    @PUT
    @Path("{id}")
    @Consumes({"application/xml", "application/json"})
    public void edit(@PathParam("id") Integer id, Step entity) {
        super.edit(entity);
    }

    @DELETE
    @Path("{id}")
    public void remove(@PathParam("id") Integer id) {
        super.remove(super.find(id));
    }

    @GET
    @Path("{id}")
    @Produces({"application/xml", "application/json"})
    public Step find(@PathParam("id") Integer id) {
        return super.find(id);
    }

    @GET
    @Override
    @Produces({"application/xml", "application/json"})
    public List<Step> findAll() {
        return super.findAll();
    }

    @GET
    @Path("{from}/{to}")
    @Produces({"application/xml", "application/json"})
    public List<Step> findRange(@PathParam("from") Integer from, @PathParam("to") Integer to) {
        return super.findRange(new int[]{from, to});
    }

    @GET
    @Path("getall/{idCategory}/{idLesson}")
    @Produces({"application/xml", "application/json"})
    public List<Step> getAll(@PathParam("idCategory") Integer idCategory, @PathParam("idLesson") Integer idLesson) {
        return em.createNamedQuery("Step.findByIdLesson").setParameter("idlesson", idLesson).getResultList();
    }

    @GET
    @Path("count")
    @Produces("text/plain")
    public String countREST() {
        return String.valueOf(super.count());
    }

    @Override
    protected EntityManager getEntityManager() {
        return em;
    }

}
